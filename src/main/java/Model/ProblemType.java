package Model;

public class ProblemType {
    private int id;
    private String typel;
    private float price;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getTypel() {
        return typel;
    }

    public void setTypel(String typel) {
        this.typel = typel;
    }

    public float getPrice() {
        return price;
    }

    public void setPrice(float price) {
        this.price = price;
    }
}
